;NO OF VOWELS IN A STRING
ASSUME DS:DATA,CS:CODE
DATA SEGMENT
MSG1 DB 10,13,"ENTER YOUR STRING:$"
MSG2 DB 10,13,"NUMBER OF VOWELS IS :$"
STR DB 10,?,13 DUP("$")
DATA ENDS
CODE SEGMENT
NUM DB 0
START:
MOV AX,DATA
MOV DS,ax

;PRINT MSG1
MOV AH,09h
LEA DX,MSG1
INT 21h

;READ STRING
MOV SI,0
LOOP1:
MOV AH,01h
INT 21h
CMP AL,13
JZ END1
MOV STR[SI],AL
INC SI
JMP LOOP1
END1:
MOV STR[SI],"$"


;COUNT VOWELS
MOV SI,0
LOOP2:
MOV AL,STR[SI]
CMP AL,"$"
JZ END2
CMP AL,"A"
JZ VOWEL
CMP AL,"E"
JZ VOWEL
CMP AL,"I"
JZ VOWEL
CMP AL,"O"
JZ VOWEL
CMP AL,"U"
JZ VOWEL
INC SI
JMP LOOP2
VOWEL:
INC NUM
inc si  
JMP LOOP2
END2:
;PRINT MSG 2
MOV AH,09H
LEA DX,MSG2
INT 21H
;PRINT NO OF VOWLES
MOV AL,NUM
ADD AL,"0"
MOV AH,02H
MOV DL,AL
INT 21H
;TERMINATE PROGRAM
MOV AH,4Ch
INT 21H

CODE ENDS
END START