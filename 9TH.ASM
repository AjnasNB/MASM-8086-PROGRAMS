; PROGRAM TO FIND THE REVERSE
ASSUME DS:DATA,CS:CODE
DATA SEGMENT
MSG1 DB 10,13,"ENTER THE STRING:$"

MSG2 DB 10,13,"THE REVERSED STRING IS:$"

STR DB 10,?,13 DUP('$')
REVERSE DB 20,?,20 DUP('$')

DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: 
MOV AX,DATA
MOV DS,AX

;MSG1
MOV AH,09h
LEA DX,MSG1
INT 21h

;INPUT STRING
MOV SI,0

LOOP1:
MOV AH,01h
INT 21h
CMP AL,10
JZ END1
MOV STR[SI],AL
INC SI
JMP LOOP1

END1:
;PRINT MSG2
MOV AH,09h
LEA DX,MSG2
INT 21h

;REVERSE STRING
LOOP2:
MOV AH,02h
MOV DL,STR[SI]
INT 21h
MOV REVERSE[SI],DL
DEC SI
CMP SI,-1
JZ TERMINATE
JMP LOOP2
;TERMINATE PROGRAM
TERMINATE:
MOV AH,4Ch
INT 21h

CODE ENDS
END START

