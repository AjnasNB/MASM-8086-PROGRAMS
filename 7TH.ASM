; PGRM TO ENTER AN INPUT STRING AND DISPLAY IT
ASSUME DS:DATA,CS:CODE
DATA SEGMENT
MSG DB 10,13,"ENTER A STRING :$"
STR DB 20 DUP("$")
LEN DW 0
DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX

MOV AH,09h ;MSG1
LEA DX,MSG
INT 21h

MOV SI,0 ;SOURCE INDEX

LOOPS:  ;LOOP
MOV AH,01h;INPUT STRING
INT 21H
CMP AL,13
JE SKIP 
MOV STR[SI],AL
INC SI 
JMP LOOPS
SKIP:
   MOV STR[SI],'$'
MOV LEN,SI


;DISPLAY STRING
MOV AH,09H
LEA DX,STR
INT 21h

;TERMINATE PROGRAM
MOV AH,4Ch
INT 21h

CODE ENDS
END START