; PROGRAM TO CONCATENATE TWO STRINGS
ASSUME DS:DATA,CS:CODE
DATA SEGMENT
MSG1 DB 10,30,"ENTER 1ST STRING:$"
MSG2 DB 10,30,"ENTER 2ND STRING:$"
MSG3 DB 10,30,"THE CONCATENATED STRING IS:$"

STR1 DB 10,?,13 DUP('$')
STR2 DB 10,?,13 DUP('$')
RESULT DB 20,?,20 DUP('$')

DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX

;PRINT MSG1
MOV AH,09h
LEA DX,MSG1
INT 21h


MOV SI,0
;READ STR1 
LOOP1:
MOV AH,01H
INT 21h
CMP AL,13
JE LOOP2
MOV RESULT[SI],AL
INC SI
JMP LOOP1



LOOP2:
;PRINT MSG2
MOV AH,09h
LEA DX,MSG2
INT 21h

;READ STR2
LOOP3:
MOV AH,01H
INT 21h
CMP AL,13
JE FINAL
MOV RESULT[SI],AL
INC SI
JMP LOOP3
FINAL:
 MOV RESULT[SI],"$"



;DISPLAY MSG3
MOV AH,09h
LEA DX,MSG3
INT 21h
;DISPLAY CON CATENATED  STRING
MOV AH,09h
LEA DX,RESULT
INT 21h



;TERMINATE PROGRAM
MOV AH,4Ch
INT 21h

CODE ENDS
END START
