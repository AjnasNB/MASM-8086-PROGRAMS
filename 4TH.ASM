; PROGRAM TO MULTIPLY TWO SINGLE DIGIT NUMBERS
ASSUME CS:CODE,DS:DATA
DATA SEGMENT
MSG1 DB 10,13,"ENTER 1ST NUMBER:$"
MSG2 DB 10,13, "ENTER 2ND NUMBER:$"
MSG3 DB 10,13, "PRODUCT IS:$"
SUM DB ?

DATA ENDS


CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX

; DISPLAY MESSAGE 1
MOV AH,09H
LEA DX,MSG1
INT 21H

; READ 1ST NUMBER
MOV AH,01H
INT 21H
sub al,"0" ;ASCII TO HEX
MOV BH,AL

; DISPLAY MESSAGE 2

MOV AH,09H
LEA DX,MSG2
INT 21H

; READ 2ND NUMBER
MOV AH,01H
INT 21H
sub al,"0" ;ASCII TO HEX
MOV BL,AL


; ADD THE TWO NUMBERS
MUL BH ; 2ND NUMBER IS STILL IN AL
MOV SUM,AL ;STORED SUM OF NUMBERS IN SUM


MOV AH,0 ; AFTER USING AAM THE NUMBERS WILL BE STORED IN AG AND AL
AAM ; CONVERTS BINARY TO BCD

ADD AL,"0" ;HEX TO ASCII
ADD AH,"0" ;HEX TO ASCII
MOV BX,AX



; DISPLAY MESSAGE 3
MOV AH,09H
LEA DX,MSG3
INT 21H

; DISPLAY THE SUM
MOV AH,02H
MOV DL,BH ; DISPLAY 1ST BIT
INT 21H

MOV AH,02H
MOV DL,BL ;DISPLAY 2ND BIT
INT 21H
;TERMINATE THE PROGRAM
MOV AH,4CH
INT 21H



CODE ENDS
END START