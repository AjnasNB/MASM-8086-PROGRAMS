;Input a number and print it
ASSUME DS:DATA,CS:CODE
DATA SEGMENT
MSG DB 10,13, "ENTER NUMBER!$"
MSG1 DB 10,13, "ENTERED NUMBER IS $"

DATA ENDS ;DATA SEGMENT ENDS


CODE SEGMENT ;CODE BEGINS
START:
MOV AX,DATA
MOV DS,AX

MOV AH,09H
LEA DX,MSG
INT 21H  ;PRINTS THE STRING IN MSG WITH 09 FUNCTION

MOV AH,01H
INT 21H ;TAKES INPUT FROM THE USER

MOV AH,09
LEA DX,MSG1
INT 21H ; PRINTS STRING WITH 09H FUNCTION

MOV AH,02H
MOV DL,AL
INT 21H ;PRINTS THE INPUT FROM THE USER

MOV AH,4CH
INT 21H ;TERMINATE THE PROGRAM WITH 4CH


CODE ENDS
END START